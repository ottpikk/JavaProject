package BasicPractice;

public class SwitchCaseBrake {
    public static void main(String[] args) {
        int x = 3;
        switch (x){
            case 1:
                System.out.println("Red");
                break;
            case 2:
                System.out.println("Blue");
                break;
            default:
                System.out.println("No colour");
        }
    }
}
